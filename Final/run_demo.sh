#!/usr/bin/env bash
# Запуск демо для жюри. Сайт станет доступен всем в общей Wi-Fi-сети.
# Использование:  bash Final/run_demo.sh   (или ./Final/run_demo.sh)
set -e
cd "$(dirname "$0")"   # переходим в Final/, где лежит docker-compose.yml

echo "=================================================================="
echo " Адрес(а) этого ноутбука в сети (172.17-20.0.1 — это Docker, не они):"
hostname -I | tr ' ' '\n' | grep -vE '^$' | sed 's/^/   /'
echo
echo " Жюри в этом же Wi-Fi открывают в браузере:"
echo "     http://<адрес выше>:8000/           — версия для человека (B2C)"
echo "     http://<адрес выше>:8000/business    — версия для бизнеса (B2B)"
echo
echo " Обычно нужный адрес: 192.168.x.x (Android-раздача) или 172.20.10.x (iPhone)."
echo "=================================================================="
echo
echo " ВНИМАНИЕ: порт 8000 должен быть свободен — сначала останови dev-сервер,"
echo " если он ещё запущен (иначе контейнер не поднимется)."
echo
echo " Первый запуск собирает образ (несколько минут). Ctrl+C — остановить."
echo
docker compose up --build
